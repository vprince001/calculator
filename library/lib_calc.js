let add = function(a,b){
  return a+b;
};

let sub = function(a,b){
  return a-b;
};

let multiply = function(a,b){
  return a*b;
};

let divide = function(a,b){
  return a/b;
};

let mod = function(a,b){
  return a%b;
};

exports.add = add;
exports.sub = sub;
exports.multiply = multiply; 
exports.divide = divide;
exports.mod = mod;
